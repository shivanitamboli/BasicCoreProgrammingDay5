package com.logicalprogram;

public class Swap {

	public static void main(String[] args) {
    int a=7,b=8,temp;
    System.out.println("1st no before swap:"+a);
    System.out.println("2nd no before swap:"+b);
   
    temp=a;
    a=b;
    b=temp;
    
    System.out.println("1st no after swap:"+a);
    System.out.println("1st no after swap:"+b);


	}

}
